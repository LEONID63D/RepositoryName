def findex(list, fitem):
    count = 0
    ind = -1
    for name in list:
        ind += 1
        if name == fitem:
            count +=1
            return ind
            break
    if count != 0:
        return None


items_list = ['яблоко', 'банан', 'апельсин', 'груша', 'киви', 'банан']

for find_item in ['банан', 'груша', 'персик']:
    index_item = findex(items_list, find_item)  # TODO Вызовите функцию, что получить индекс товара
